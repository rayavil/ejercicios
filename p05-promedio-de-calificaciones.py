# Calcular el promedio de 3 calificaciones

print("Calculando el promedio de 3 calificaciones:\n")
c1 = float(input("Calificacion 1: "))
c2 = float(input("Calificacion 2: "))
c3 = float(input("Calificacion 3: "))
suma = (c1 + c2 + c3 ) 
prom = suma / 3
print(f"El promedio de: {c1} {c2} {c3} es {prom} y la suma es {suma}")